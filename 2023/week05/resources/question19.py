liste_finale = []
tuple_initial = (1, 4, 5, 8)
for valeur in tuple_initial:          # A chaque iteration valeur devient une copie d'un element du tuple. La boucle se termine au dernier élément de tuple_initial.
	liste_finale.append(valeur)   # On ajoute valeur à la liste créée en dehors de la boucle.